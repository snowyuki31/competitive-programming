vector<ll> divisor(ll n) {
    vector<ll> ret;
    for(ll i = 1; i * i <= n; i++) {
        if(n % i == 0) {
            ret.push_back(i);
            if(i * i != n) ret.push_back(n / i);
        }
    }
    sort(begin(ret), end(ret));
    return (ret);
}

int main()
{
    cin.tie(0);
    ios::sync_with_stdio(0);
    cout << fixed << setprecision(20);

    ll N;
    cin >> N;
    vector<ll> div1, div2;
    div1 = divisor(N);
    div2 = divisor(N - 1);

    set<ll> st;

    for(auto d : div1){
        if(d == 1) continue;
        ll now = N;
        if((now - 1) % d == 0) st.insert(d);

        while(now % d == 0){
            now /= d;
            if((now - 1) % d == 0) st.insert(d);
        }
    }
    
    for(auto d : div2) if(d != 1) st.insert(d);

    ll ans = len(st);
    print(ans);

    return 0;
}