using namespace std;
using ll = long long;
typedef pair<ll, ll> P;
ll MOD = 1e9 + 7;
ll devc(ll x, ll y) { return 1 + (x - 1) / y; }

int main()
{
    cin.tie(0);
    ios::sync_with_stdio(0);
    cout << fixed << setprecision(20);

    ll N;
    cin >> N;

    if(N == 0){
        print(0);
        return 0;
    }

    string s = "";
    ll now = 1;
    while(N != 0){
        ll k = abs(now * 2);
        ll d = (N % k + k) % k;
        ll c = d / abs(now);

        s = to_s(c) + s;
        N -= now * c;
        now *= -2;
    }

    print(s);

    return 0;
}